# a = 10, a adalah variabel dengan nilai 10

# tipe data : angaka satuan yang gak ada komanya (integer)
data_integer = 10
print ("data :", data_integer)
print ("_ bertipe :", type(data_integer))

# tipe data : angka dengan koma(float)
data_float = 1.5
print ("data :", data_float)
print ("_ bertipe :", type(data_float))

# tipe data : kumpulan karakter (string)
data_string = "yoga_wahyudi"
print ("data :", data_string)
print ("_ bertipe :", type(data_string))

# tipe data : biner true/false(boolean)
data_bool = True
print ("data :", data_bool)
print ("_ bertipe :", type(data_bool))

# tipe data khusus

# bilangan kompleks = j adalah imajiner.realnya didepan j nya dibelakang
data_complex = complex(5,6)
print("data :", data_complex)
print ("- bertipe :", type(data_complex))