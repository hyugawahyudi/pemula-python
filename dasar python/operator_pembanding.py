# operator pembanding
""" 1. digunakan untuk membandingkan 2 nilai
    2. nilai yang dihasilkan adalah (Tru/False)
    
    operator       keterangan                            contoh
    ==             sama dengan                            x == y
    !=             tidak sama dengan                      x != y
    >              lebih besar dari                       x > y
    <              lebih kecil dari                       x < y
    >=             lebih besar dari atau sama dengan      x >= y
    <=             lebih kecil dari atau sama dengan      x <= y"""

x = 5
y = 2

print(x != 5)
print(x != y)

print("apakah x lebih besar dari y?",x > y)
print("apakah x lebih kecil dari y?",x < y)
print("apakah x lebih besar dari atau sama dengan y?",x >= y)
print("apakah x lebih kecil dari atau sama dengan y?",x <= y)
